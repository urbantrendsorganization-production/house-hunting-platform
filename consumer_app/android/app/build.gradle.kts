// Google Maps key comes from android/local.properties (never committed):
//   MAPS_API_KEY=AIza...
// Falls back to empty so the project builds without a key (map degrades to list).
val mapsApiKey: String = run {
    val props = Properties()
    val f = rootProject.file("local.properties")
    if (f.exists()) f.inputStream().use { props.load(it) }
    props.getProperty("MAPS_API_KEY", "")
}

